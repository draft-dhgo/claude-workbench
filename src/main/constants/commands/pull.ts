// Command: pull — 원격 저장소 최신화 및 변경사항 병합 (Korean)

export const CMD_PULL = `원격 저장소의 최신 변경사항을 가져와 현재 브랜치에 병합합니다.

사용자 입력: $ARGUMENTS

## 실행 절차

1. 현재 워크스페이스의 Git 상태를 확인한다
   - Git 저장소가 아니면 "Git 저장소가 아닙니다."를 출력하고 종료한다
2. 로컬 미커밋 변경사항(uncommitted changes)이 있는지 확인한다
   - 미커밋 변경사항이 있으면 사용자에게 경고하고 진행 여부를 확인한다
   - 사용자가 취소하면 종료한다
3. $ARGUMENTS가 비어있으면:
   - 현재 워크스페이스에 포함된 모든 저장소(워크트리)를 대상으로 일괄 pull을 수행한다
   - 각 저장소별로 현재 브랜치의 원격 저장소에서 fetch + merge를 실행한다
4. $ARGUMENTS가 비어있지 않으면:
   - 지정된 저장소/경로에 대해서만 pull을 수행한다
5. 각 저장소에 대해:
   a. \`git fetch origin\`을 실행하여 원격 변경사항을 가져온다
   b. \`git merge origin/{현재브랜치}\`를 실행하여 병합한다
   c. 충돌이 발생하면:
      - 충돌 파일 목록을 사용자에게 표시한다
      - 충돌 해결 방법을 안내한다 (수동 해결 필요)
      - \`git merge --abort\`로 병합을 취소할 수 있음을 안내한다
6. 결과를 출력한다:
   - 각 저장소별 pull 결과 (성공/충돌/에러)
   - 가져온 커밋 수, 변경된 파일 수 요약

## 규칙

- 원격 저장소 설정이 없는 경우 해당 저장소를 건너뛴다
- 충돌 발생 시 자동 해결하지 않고 사용자에게 알린다
- 각 저장소의 pull 결과를 개별적으로 보고한다
- 미커밋 변경사항 경고 시 stash 옵션을 안내한다
`
