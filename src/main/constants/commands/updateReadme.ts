// Command: update-readme — README.md 문서·스크린샷 현행화 (Korean)

export const CMD_UPDATE_README = `README.md 문서와 스크린샷을 현행화합니다.

사용자 입력: $ARGUMENTS

## 실행 절차

1. 워크스페이스 루트의 README.md 파일을 읽는다
   - 파일이 없으면 "README.md 파일이 없습니다."를 출력하고 종료한다
2. $ARGUMENTS가 비어있지 않으면:
   - 해당 섹션(마크다운 ## 헤더 기준)을 찾아 해당 영역만 분석한다
   - 섹션을 찾을 수 없으면 사용 가능한 섹션 목록을 보여주고 종료한다
3. $ARGUMENTS가 비어있으면:
   - README.md 전체를 스캔하여 모든 섹션을 분석한다
4. 각 섹션에 대해:
   - 현재 프로젝트의 실제 코드, 기능, 구조와 문서 내용을 비교한다
   - 불일치 항목(누락된 기능, 변경된 사용법, 오래된 설명 등)을 목록으로 정리한다
5. 스크린샷 참조가 있는 경우:
   - 스크린샷 파일의 존재 여부를 확인한다
   - 현재 앱 상태와 스크린샷이 다를 수 있음을 안내한다
6. 변경이 필요한 항목을 사용자에게 보여준다:
   - 각 변경 항목의 before/after를 명확히 제시한다
7. 사용자 확인 후 README.md를 수정한다
8. 변경 사항 요약 리포트를 출력한다:
   - 수정된 섹션 수
   - 추가/수정/삭제된 내용 요약

## 규칙

- README.md 외 다른 파일은 수정하지 않는다
- 변경 전 반드시 사용자에게 변경 내용을 보여주고 확인을 받는다
- 프로젝트 소스 코드를 읽어 실제 기능과 문서의 정합성을 검증한다
- 기존 문서 구조(헤더 레벨, 순서)를 최대한 유지한다
`
